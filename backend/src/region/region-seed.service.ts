  private async seedData(level: string, items: [number | string, string, string][]) {
    const lv = level === 'city' ? 2 : level === 'district' ? 3 : 4
    for (const [parentRef, name, code] of items) {
      let parent: AddressRegion | null = null
      if (typeof parentRef === 'number') {
        parent = await this.repo.findOne({ where: { id: parentRef, level: 1 } })
      } else if (lv === 3) {
        // district: parent is city, match by city code (first 4 chars)
        parent = await this.repo.findOne({ where: { code: parentRef.substring(0, 4), level: 2 } })
      } else if (lv === 4) {
        // street: parent is district, match by full district code
        parent = await this.repo.findOne({ where: { code: parentRef, level: 3 } })
      }
      if (!parent) {
        console.warn(`[RegionSeed] 未找到父级: ${parentRef} -> ${name} (level=${lv})`)
        continue
      }
      await this.repo.save({ parentId: parent.id, name, level: lv, code })
    }
  }

